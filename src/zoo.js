const data = require('./data');

function animalsByIds(incomingArray) {
  let finalArray = [];

  if (!Array.isArray(incomingArray) || incomingArray.length === 0) {
    return finalArray;
  }
  incomingArray.map((item) => {
    const animals = data.animals.filter(animal => animal.id === item);

    if (animals) {
      finalArray = [
        ...finalArray,
        animals,
      ];
    }
    return finalArray;
  });
  return finalArray;
}

